# Lets plot some data for Uganda from collapse::wlddev datase
library(collapse)
library(magrittr) # Pipe operators
library(plotly)

wldug <- wlddev %>% fsubset(country == "Uganda") 

 plot_ly(wldug,
    x = ~ year,
    y = ~ PCGDP
  )

 plot_ly(wldug,
   x = ~ year,
   y = ~ PCGDP, 
   type = "scatter",
   mode = "markers+lines"
 )

  plot_ly(wldug,
    x = ~ year,
    y = ~ PCGDP, 
    type = "scatter",
    mode = "markers+lines", 
    name = "GDP per capita",
    hoverinfo = "x+y+name"
  ) %>% 
  layout(yaxis = list(title = "GDP per capita (constant 2010 US$)"), 
         hovermode = "compare")

# We can add a second variable using the add_trace function:
  
  plot_ly(wldug,
          x = ~ year,
          y = ~ PCGDP, 
          type = "scatter",
          mode = "markers+lines", 
          name = "GDP per capita",
          hoverinfo = "x+y+name"
  ) %>% 
    add_trace(y = ~ LIFEEX, 
              name = "Life Expectancy") %>%
    layout(yaxis = list(title = "Value"), 
           hovermode = "compare")
  
# This however does not make a lot of sense, as the variable are on different scales
# We could put them on different scales:

  plot_ly(wldug,
          x = ~ year,
          y = ~ PCGDP, 
          type = "scatter",
          mode = "markers+lines", 
          name = "GDP per capita",
          hoverinfo = "x+y+name"
  ) %>% 
    add_trace(y = ~ LIFEEX, 
              name = "Life Expectancy", 
              yaxis = "y2") %>%
    layout(title = "GDP Per Capita and Life Expectancy in Uganda, 1960-2017",
           yaxis = list(title = "GDP per capita (constant 2010 US$)", 
                        tickformat = "$",
                        hoverformat = '$.1f'), 
           yaxis2 = list(
             overlaying = "y",
             side = "right",
             title = "Life expectancy at birth, total (years)",
             hoverformat = '.2f'
           ),
           xaxis = list(title = "Year", showgrid = FALSE),
           hovermode = "compare", 
           legend = list(x = 0.04, y = 0.95), 
           margin = list(
             l = 60, r = 60,
             b = 40, t = 40,
             pad = 2
           ))
